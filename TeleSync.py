def split_chunks(text: str, limit: int = 4000):
    """Split a long string into Telegram-safe chunks (â‰¤ limit chars), by line."""
    lines = text.splitlines(keepends=True)
    chunks = []
    current = ""
    for line in lines:
        if len(current) + len(line) > limit:
            chunks.append(current)
            current = line
        else:
            current += line
    if current:
        chunks.append(current)
    return chunks

async def delete_previous(chat_id: int, ctx: ContextTypes.DEFAULT_TYPE):
    ids = ctx.user_data.get('last_message_ids', [])
    for msg_id in ids:
        try:
            await ctx.bot.delete_message(chat_id, msg_id)
        except:
            pass
    ctx.user_data['last_message_ids'] = []

